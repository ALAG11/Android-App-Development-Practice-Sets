package com.example.question_5;

public class Question_5 {
    public static void main(String[] args) {
        int sum = 0;
        int i = 0;
        int n = 10;
        // Calculating the sum
        while (i<n){
            sum += 2*i;
            i++;
        }
        System.out.println("The sum is " + sum + ".");

    }
}
